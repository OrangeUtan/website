<script lang="ts">
	function toggleTheme() {
		dark = !dark;
		const theme = dark ? 'dark' : 'light';
		document.documentElement.setAttribute('data-theme', theme);
	}

	let dark = false;
</script>

<nav class="flex justify-between align-middle pt-3 pb-3 pr-6 pl-6 shadow-md">
	<a href="/" class="text-xl">
